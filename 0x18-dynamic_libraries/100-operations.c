#include <stdio.h>
#include "100-main.h"

/**
 * add - calculates mod
 *
 * @a: first parameter
 * @b: second parameter
 * Return: result as int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - calculates mod
 *
 * @a: first parameter
 * @b: second parameter
 * Return: result as int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - calculates mod
 *
 * @a: first parameter
 * @b: second parameter
 * Return: result as int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - calculates div
 *
 * @a: first parameter
 * @b: second parameter
 * Return: result as int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculates mod
 *
 * @a: first parameter
 * @b: second parameter
 * Return: result as int
 */
int mod(int a, int b)
{
	return (a % b);
}
